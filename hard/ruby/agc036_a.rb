# 固定

S = gets.to_i

# [x1, y1] = [0, 0]で固定すると
# 三角形の面積は (x2 * y3 - y2 * x3).abs / 2で求まる
# x2 = 10 ** 9, y2 = 1 を固定する
# y3 = (S + x2 - 1) / x2
# x3 = x2 * y3 - S

x1 = y1 = 0
x2 = 10 ** 9
y2 = 1

y3 = (S + x2 - 1) / x2
x3 = x2 * y3 - S

puts [x1, y1, x2, y2, x3, y3].join(" ")
